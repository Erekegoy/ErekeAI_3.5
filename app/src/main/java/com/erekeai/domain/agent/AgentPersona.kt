package com.erekeai.domain.agent

/**
 * Роль (персона), в которой работает [AgentOrchestrator]. Влияет только на системный промпт —
 * набор инструментов (ToolRegistry) остаётся общим, но для DEVELOPER модель получает
 * дополнительные инструкции по работе с кодом, проверками и Git/GitHub.
 */
enum class AgentPersona {
    /** Обычный ассистент общего назначения. */
    GENERAL,

    /**
     * 🤖 AI Developer Agent — специализированная персона для разработки:
     * анализ проекта и структуры кода, генерация и рефакторинг кода,
     * запуск проверок и разбор логов, работа с Git/GitHub, планирование задач.
     */
    DEVELOPER
}
